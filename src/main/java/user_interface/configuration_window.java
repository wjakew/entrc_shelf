    /**
     * Function for loading window
     */
    void load_window(){
        label_version.setText("ENTRC_SHELF");
        if ( !configuration.file_found ){
            button_question.setEnabled(false);
        }
        if ( configuration.error ){
            field_url.setText("");
            field_apptoken.setText("");
            field_shelf_name.setText("");
        }
        else{
            if ( configuration.standard_file ){
                field_url.setText(configuration.url);
                field_apptoken.setText(configuration.apptoken);
                field_shelf_name.setText("");
                field_url.setEnabled(false);
                field_apptoken.setEnabled(false);
            }
            else{
                field_url.setText(configuration.url);
                field_apptoken.setText(configuration.apptoken);
                field_shelf_name.setText(configuration.entrc_ic_drawer_code);
                field_url.setEnabled(false);
                field_apptoken.setEnabled(false);
                field_shelf_name.setEnabled(false);
            }
        }
    }
    
    /**
     * Function for validating fields
     * @return Boolean
     */
    boolean validate_fields(){
        ArrayList<JTextField> fields = new ArrayList<>();
        fields.add(field_url);
        fields.add(field_shelf_name);
        fields.add(field_apptoken);
        
        for(JTextField field : fields){
            if ( field.getText().equals("")){
                return false;
            }
        }
        return true;
    }

