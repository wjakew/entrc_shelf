    private void button_load_config_fileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_load_config_fileActionPerformed
        if ( validate_fields() ){
            configuration.entrc_ic_drawer_code = field_shelf_name.getText();
            Connector connector = new Connector(configuration);
            try{
                Parser parser = new Parser(connector.health());
                new message_window(this,true,"Połączono z entrcAPI\nwersja:"+parser.get_string("program_version"),"API Podłączone");
                // main window ready to connect
                // ready to load drawer content
                new main_window(connector,configuration);
                dispose();
            } catch (UnirestException ex) {
                new message_window(this,true,"Błąd\n"+ex.toString(),"Błąd aplikacji");
                dispose();
                // error checking connection to the database
            } catch(NullPointerException e){
                new message_window(this,true,"Żądany serwer API nie odpowiada","Błąd połączenia");
            }
        }
        else{
            new message_window(this,true,"Błędnie wypełniony formularz","Błąd formularza");
        }
    }//GEN-LAST:event_button_load_config_fileActionPerformed

